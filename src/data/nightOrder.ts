export function getNightOrderSteps(
  scriptId: string,
  phase: NightOrderPhase,
): NightOrderStep[] {
  return troubleBrewingNightOrder
    .filter((step) => step.scriptId === scriptId && step.phase === phase)
    .sort((a, b) => a.order - b.order);
}